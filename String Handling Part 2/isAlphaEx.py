# isAlpha Example.py

# Example 1
a="apple"
print(a.isalpha()) # True

# Example 2
a="APPLE"
print(a.isalpha()) # True

# Example 3
a="python 3.14"
print(a.isalpha()) #False

# Example 4
a="python lower"
print(a.isalpha()) #False

# Example 5
a="$python"
print(a.isalpha()) #False

# Example 6
a="12345"
print(a.isalpha()) #False

# Example 7
a="!@#$%^&*"
print(a.isalpha()) #False








