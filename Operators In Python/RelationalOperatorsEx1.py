# WAPP which demonstrate the functionality os relational assignment
# RelationalAssignmentEx.py
a=float(input("Enter Value of a:"))
b=float(input("Enter Value of b:"))
print("\t Result Of Relational Operators And Expressions:")
print("\t\t{}>{}={}".format(a,b,a>b))
print("\t\t{}<{}={}".format(a,b,a<b))
print("\t\t{}=={}={}".format(a,b,a==b))
print("\t\t{}!={}={}".format(a,b,a!=b))
print("\t\t{}>={}={}".format(a,b,a>=b))
print("\t\t{}<={}={}".format(a,b,a<=b))


