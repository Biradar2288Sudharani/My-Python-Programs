# WAPP which will accept two numerical values & decide biggest among them & check for equality by using simple if statement
# Simple_if_StmtEx1.py
a=int(input("Enter First Number:"))
b=int(input("Enter Second Number:"))
if(a>b):
    print("Biggest Number ({},{})={}".format(a,b,a))
if(b>a):
    print("Biggest Number ({},{})={}".format(a,b,b))
if(a==b):
    print("Both Numbers Are Equal")
