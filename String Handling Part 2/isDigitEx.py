# isDigit Example.py

# Example 1
a="apple"
print(a.isdigit()) # True

# Example 2
a="3.234"
print(a.isdigit()) # False

# Example 3
a="python 3.14"
print(a.isdigit()) #False

# Example 4
a="123_123"
print(a.isdigit()) #False

# Example 5
a="$123424"
print(a.isdigit()) #False

# Example 6
a="12345"
print(a.isdigit()) #True

# Example 7
a="!@#$%^&*"
print(a.isdigit()) #False

















































