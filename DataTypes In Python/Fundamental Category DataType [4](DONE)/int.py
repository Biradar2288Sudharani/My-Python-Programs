a=23
print(a,type(a))  # 23, <class 'int'>

# examples of binary number system
a=0b1011
print(a,type(a))  # 11 <class 'int'>
a=0B1011
print(a,type(a))  # 11 <class 'int'>

# convert decimal to binary by using bin() method
print(bin(10))  # 0b1010
print(bin(11))  # 0b1011
print(bin(13))  # 0b1101
print(bin(15))  # 0b1111

# examples of octal number system
a=0o17
print(a,type(a))  # 15 <class 'int'> 
a=0O17
print(a,type(a))  # 15 <class 'int'> 
a=0o123
print(a,type(a))  # 83 <class 'int'> 
a=0o70
print(a,type(a))  # 56 <class 'int'> 
a=0o175
print(a,type(a))  # 125 <class 'int'> 

# convert decimal to octal by using oct() method
print(oct(27))  # 0o33
print(oct(83))  # 0o123
print(oct(15))  # 0o17
print(oct(23))  # 0o27









