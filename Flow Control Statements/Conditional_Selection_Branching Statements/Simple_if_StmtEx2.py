# WAPP which will accept any number & decide Positive, Negative or Zero. by using simple if statement
# Simple_if_StmtEx2.py
a=int(input("Enter First Number:"))
if(a>0):
    print("Number({}) is Positive={}".format(a,a))
if(a<0):
    print("Number({}) is Negative={}".format(a,a))
if(a==0):
    print("Number Is Zero")
print("Program Execution Is Completed!!")