# program for demonstrating round function 
a=3.5687459478944895   # Positive Round Fuction
print(round(a,4))

print(round(a,3))

print(round(a,2))

print(round(a,1))

print(round(a,0))

a=3.5687459478944895   # Negative Round Fuction
print(round(a,-4))

print(round(a,-3))

print(round(a,-2))

print(round(a,-1))

print(round(a,0))
