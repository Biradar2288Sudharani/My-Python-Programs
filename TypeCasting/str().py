# SYNTAX : varname(int|float|bool|complex)

#EXAMPLE-1: int to str
a=12
print(a,type(a))
b=str(a)
print(b,type(b))
print(b)

#EXAMPLE-3: float to str
a=12.34
print(a,type(a))
b=str(a)
print(b,type(b))
print(b)

#EXAMPLE-3: bool to str
a=True
print(a,type(a))
b=str(a)
print(b,type(b))
print(b)

#EXAMPLE-4: complex to str
a=12+23j
print(a,type(a))
b=str(a)
print(b,type(b))
print(b)
