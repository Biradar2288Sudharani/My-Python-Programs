# CapitalizeExamples.py
# Example 1
s="python"
print(s,type(s)) #python <class 'str'>
print(s.capitalize()) #Python

# Example 2
s="python is an oop language"
print(s,type(s)) #python is an oop language <class 'str'>
print(s.capitalize()) #Python is an oop language

# Example 3
a="python"
print(a,type(a)) #python <class 'str'>
print(a.capitalize(),type(a)) #Python <class 'str'>
s=a.capitalize()
print(s,type(s)) #Python <class 'str'>























